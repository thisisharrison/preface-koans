// traditional functions
function foo() {
    console.log("foo");
}
foo();

// arrow functions
const foo1 = () => {
    console.log("foo");
};
foo1();

// how to return a value in a traditional function
function bar() {
    return "bar";
}
x = bar();
x;

// how to return a value in an arrow function
const bar1 = () => "bar";
x = bar1();
x;

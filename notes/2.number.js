// NUMBER

// Ways to Add One (increment)
let a = 1;
a = a + 1;
a;

a += 1; // a = a + 1
a;

// increment and return original
console.log(a++);
// we have incremented it above, now a is 4
a;

// increment and get the new value
console.log(++a);

// Ways to Minus One (decrement)
a -= 1; // a = a - 1
a--; // minus 1 and returns the original
--a; // return the new value

// Same applies to Multiply and Divide
a = a * 100;
// a *= 100

a = a / 2;
// a /= 2
